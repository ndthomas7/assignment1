public class NautMile
{
   //The goal of this program is to covert the number of nautical miles to kilometers and miles
   
   public static void main (String [] args)
   {
      int nauticalMiles = 7; //I chose 7 nautical miles because it's my favorite number
      
      final double KILOMETERS = 1.852; // 1 nautical mile = 1.852 kilometers
       
      final double MILES = 1.150779; // 1 nautical mile = 1.150779 miles
      

      //calculating nautical miles to kilometers and miles
      
      System.out.println(nauticalMiles + " nautical miles is equivalent to "
      
                         + KILOMETERS*7 + " kilometers or " + MILES*7 + " miles.");
   
   }
}   